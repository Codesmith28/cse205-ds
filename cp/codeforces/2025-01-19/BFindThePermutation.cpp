void solve( ) {
    int n;
    cin >> n;
    vector<vector<int>> adj(n, vector<int>(n, 0));
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> adj[i][j];
        }
    }

    // indeg => there are indeg[i] elements greater than i

    vector<int> indeg(n, 0);
    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            indeg[j] += adj[i][j];
        }
    }

    dbg(indeg);
}

