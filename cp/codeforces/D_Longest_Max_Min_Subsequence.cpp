void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int &i : a) cin >> i;

    unordered_set<int> st(a.begin(), a.end());

    vector<int> ans(begin(st), end(st));
    cout << ans.size() << endl;
    for (int i : ans) cout << i << " ";
    cout << endl;
}

