void solve() {
    string s;
    cin >> s;

    int cnt = 0;
    for (char c = 'a'; c <= 'c'; c++) {
        if (s[c - 'a'] != c) cnt++;
    }

    cout << (cnt > 2 ? "NO" : "YES") << endl;
}

